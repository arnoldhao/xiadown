type XiaText = ReturnType<typeof buildText>;

export function getXiaText(language?: string): XiaText {
  return buildText(isZhLanguage(language) ? "zh-CN" : "en");
}

export function isZhLanguage(language?: string) {
  return (language ?? "").toLowerCase().startsWith("zh");
}

export function resolveLibraryCoverFile(
  library?: LibraryDTO | null,
): LibraryFileDTO | null {
  const items = (library?.files ?? [])
    .filter(
      (file) =>
        (file.kind ?? "").trim().toLowerCase() === "thumbnail" &&
        !file.state.deleted,
    )
    .filter((file) => Boolean(file.storage.localPath?.trim()));
  return items[0] ?? null;
}

export function resolveLibraryCoverURL(
  baseURL: string,
  library?: LibraryDTO | null,
) {
  const path =
    resolveLibraryCoverFile(library)?.storage.localPath?.trim() ?? "";
  return buildAssetPreviewURL(baseURL, path);
}

export function isAudioLibraryFile(file?: LibraryFileDTO | null) {
  if (!file) {
    return false;
  }
  const kind = (file.kind ?? "").trim().toLowerCase();
  if (kind === "audio") {
    return true;
  }
  if (kind === "transcode") {
    return Boolean(file.media?.audioCodec || file.media?.channels);
  }
  const path = (file.storage.localPath ?? "").toLowerCase();
  return [
    ".aac",
    ".aif",
    ".aiff",
    ".alac",
    ".caf",
    ".flac",
    ".m4a",
    ".m4b",
    ".mp3",
    ".mp4",
    ".mpga",
    ".oga",
    ".ogg",
    ".opus",
    ".wav",
    ".wave",
    ".weba",
    ".webm",
  ].some((suffix) => path.endsWith(suffix));
}
